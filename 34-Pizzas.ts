// Define an array of favorite pizza names
let pizzas = ["Pepperoni", "Margherita", "BBQ Chicken"];

// Print statements about each pizza using a for loop
console.log("My favorite pizzas:");
for (let pizza of pizzas) {
    console.log(`- I like ${pizza} pizza.`);
}

// Print a closing statement expressing love for pizza
console.log("\nI really love pizza!");
